public class SwitchPractice {

    public static void main(String[] args) {

        String fruit = "Grape";

        switch (fruit) {

            case "Apple":
                System.out.println("Red");

                break;

            case "Orange":
                System.out.println("Orange");

                break;

            case "Banana":
                System.out.println("Yellow");

                break;

            case "Grape":
                System.out.println("Green");

                break;

            case "Plum":
                System.out.println("Purple");


        }
    }
}
